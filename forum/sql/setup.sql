begin
  declare ret_val int;
	if((select count(*) from users where username = in_username) = 0) then
		if((select count(*) from users where email = in_email) = 0) then
			insert into users(email,username,password,created_date) values (in_email,in_username,sha2(in_password,256),current_date());
            set ret_val = 0;
		else
			set ret_val = 1;
		end if;
	else
		set ret_val = 2;
	end if;
  select ret_val;
end //

create function total_activity(UID int) returns int
begin
	declare u_posts int;
    declare u_comments int;
    declare activity int;
    set u_posts = (select count(*) from posts where user_id = UID);
    set u_comments = (select count(*) from comments where user_id = UID);
    set activity = u_posts + u_comments;
    return activity;
end//
